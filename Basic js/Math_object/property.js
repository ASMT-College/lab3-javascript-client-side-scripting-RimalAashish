// Math.E - Euler's number, the base of natural logarithms (e)
console.log(Math.E);

// Math.PI - The mathematical constant π (pi)
console.log(Math.PI);

// Math.SQRT2 - Square root of 2 (√2)
console.log(Math.SQRT2);

// Math.SQRT1_2 - Square root of 1/2 (√½)
console.log(Math.SQRT1_2);

// Math.LN2 - Natural logarithm of 2 (ln(2))
console.log(Math.LN2);

// Math.LN10 - Natural logarithm of 10 (ln(10))
console.log(Math.LN10);

// Math.LOG2E - Base-2 logarithm of e (log₂(e))
console.log(Math.LOG2E);

// Math.LOG10E - Base-10 logarithm of e (log₁₀(e))
console.log(Math.LOG10E);
