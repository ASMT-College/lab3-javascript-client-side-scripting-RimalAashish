// Math.abs(x) - Returns absolute value
console.log(Math.abs(-5));

// Math.ceil(x) - Rounds up to the nearest integer
console.log(Math.ceil(4.3));

// Math.floor(x) - Rounds down to the nearest integer
console.log(Math.floor(4.7));

// Math.round(x) - Rounds to the nearest integer
console.log(Math.round(4.5));

// Math.sqrt(x) - Returns square root
console.log(Math.sqrt(16));

// Math.pow(x, y) - Returns x to the power of y
console.log(Math.pow(2, 3));

// Math.min(x, y, ...) - Returns the smallest value
console.log(Math.min(10, 20, 5));

// Math.max(x, y, ...) - Returns the largest value
console.log(Math.max(10, 20, 5));

// Math.random() - Generates a random number between 0 and 1
console.log(Math.random());

// Math.trunc(x) - Returns the integer part of a number
console.log(Math.trunc(4.9));  
